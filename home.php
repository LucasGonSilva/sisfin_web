                <div class="tab-pane fade show active" id="lista-01" role="tabpanel" aria-labelledby="lista-dica-01">
                    Anotar os gastos diários é a melhor forma de identificar como você está gastando seu dinheiro.
                    O processo é simples: anote a despesa e, ao lado, o valor. 
                    Para funcionar, é importante não esquecer de registrar nem mesmo os pequenos gastos, como chicletes,
                    pequenos lanches, revistas, passagens de ônibus etc. Por mais que o valor pareça muito baixo para impactar seu orçamento, 
                    quando somadas, estas pequenas despesas podem fazer a diferença no final do mês. 
                    Anotar os gastos diários também ajuda a identificar onde você está gastando o seu dinheiro, 
                    ver as categorias do orçamento em que estão as maiores despesas e onde é possível economizar.
                </div>
                <div class="tab-pane fade" id="lista-02" role="tabpanel" aria-labelledby="lista-dica-02">
                    A planilha financeira pode ser uma ferramenta muito útil para manter o controle das finanças pessoais em dia. 
                    Ela pode ser criada do zero – em um programa como Excel ou Google Sheets – ou ser baixada diretamente da internet. 
                    A primeira opção pode ser vantajosa para quem quer um documento personalizado, 
                    já que as categorias de gastos e receitas serão criadas de acordo com a sua realidade. 
                    Por outro lado, baixar um modelo pronto não exige domínio de ferramentas de tabela e também é prático e rápido: 
                    você pode escolher uma planilha alinhada com seus objetivos financeiros e necessidades (com espaço para investimentos, 
                    área para planejamento financeiro etc.). Independentemente do modelo de planilha que você escolher, 
                    é importante criar uma rotina de anotar nela todos os seus gastos e receitas e, ainda, 
                    fazer um acompanhamento semanal da situação das suas finanças. 
                    Assim, você sabe quanto ainda pode gastar no mês de acordo com as receitas ainda disponíveis.
                </div>
                <div class="tab-pane fade" id="lista-03" role="tabpanel" aria-labelledby="lista-dica-03">
                    Não tem paciência para anotar gasto por gasto e ainda ter que transferir os valores para uma planilha? 
                    Então aposte em um gerenciador financeiro para fazer o controle das suas finanças pessoais. 
                    A vantagem do sistema é que ele pode ser acessado de qualquer lugar (dependendo da opção escolhida, até mesmo pelo smartphone), 
                    mantém seus dados seguros e, ainda, pode ser atualizado automaticamente.<br>
                    O GuiaBolso é um dos principais gerenciadores financeiros do mercado. 
                    Simples de usar, ele organiza suas finanças em menos de dois minutos. 
                    Basta inserir os dados do internet banking e a ferramenta puxa todas as movimentações feitas na conta corrente e 
                    no cartão de crédito, categorizando-as automaticamente. 
                    Com isso, fica muito mais fácil saber onde é preciso cortar gastos e onde dá para economizar.
                </div>
                <div class="tab-pane fade" id="lista-04" role="tabpanel" aria-labelledby="lista-dica-04">
                    Quitar as dívidas deve ser sempre prioridade. 
                    Apenas dessa forma é possível ter uma vida financeira equilibrada e conseguir economizar para realizar seus sonhos no futuro. 
                    Por isso, além de anotar gastos e entender como funcionam suas finanças, é essencial listar tudo o que você deve e para quem: 
                    banco, cartão de crédito, amigos. Em seguida, liste o valor atual da dívida, inclusive os juros que você está pagando. 
                    Priorize sempre as dívidas mais caras – como cheque especial e cartão de crédito – 
                    e fique atento para o parcelamento não consumir nunca mais de 30% da sua renda líquida mensal.
                </div>
                <div class="tab-pane fade" id="lista-05" role="tabpanel" aria-labelledby="lista-dica-05">
                    Ter um fundo para emergências é essencial para não passar apertos caso aconteça algum imprevisto na sua vida, 
                    como um carro quebrado, uma doença inesperada ou a perda do emprego. 
                    Por isso, caso não tenha dívidas, reserve pelo menos 15% da sua renda líquida mensal para formar esta reserva financeira, 
                    que deve corresponder de três a seis meses do seu salário. Para não correr o risco de esquecer de depositar o valor, 
                    programe a transferência para uma poupança ou outra forma de aplicação para um dia próximo da data em que você recebe seu salário. 
                    Assim, você não corre o risco de cair em tentação e gastar o dinheiro.
                </div>
                <div class="tab-pane fade" id="lista-06" role="tabpanel" aria-labelledby="lista-dica-06">
                    A compra por impulso é uma das grandes inimigas da vida financeira saudável. 
                    Para evitar este hábito e manter as finanças sempre em dia, comece a planejar suas compras. 
                    Precisa de um vestido novo? Espere pelo menos 30 dias para concluir se ele é realmente necessário. 
                    Após isso, pesquise preços e, de preferência, prefira sempre pagar à vista. 
                    Além de poder negociar um desconto de até 20% você não corre o risco de cair na armadilha dos parcelamentos, 
                    que podem acabar desequilibrando seu orçamento lá na frente.
                </div>
                <div class="tab-pane fade" id="lista-07" role="tabpanel" aria-labelledby="lista-dica-07">
                    O planejamento anda de mãos dadas com o controle financeiro pessoal. 
                    Para ter tranquilidade na hora de gerenciar suas finanças, comece planejando metas de gastos para o próximo mês. 
                    Defina quanto você pode gastar em cada categoria do seu orçamento, priorizando as despesas fixas essenciais, como aluguel, 
                    condomínio e mensalidade do colégio. Em seguida, parta para as despesas variáveis, mas assim essenciais, como conta de luz, 
                    gás e supermercado. Depois de definir os gastos essenciais, parta para aqueles relacionados ao estilo de vida, 
                    como cuidados pessoas, lazer e restaurantes, sempre tomando cuidado para se manter na meta definida. 
                    Mesmo os sonhos financeiros devem ser planejados. 
                    Caso queira fazer uma viagem, por exemplo, defina o período em gostaria de ir, 
                    qual é o custo total da empreitada e quanto você deve poupar por mês para realizar este objetivo.<br>
                    Saber como controlar as finanças pessoais é uma questão de hábito. Quanto mais você praticar, 
                    mais simples ficará e, melhor, suas finanças permanecerão sempre equilibradas.
